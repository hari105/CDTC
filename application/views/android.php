<h2>National Android Developer Challenge 2016 (NADC-16)</h2>
<p align="justify">A Two days National Level  workshop on “Android Application Development” was held on Jan 22 and 23, 2015 in  AGI as the first stage of National Android Developer Challenge 2016 (NADC-16) which was held at IIT Madras in the month of March 2016. The training was conducted by Mr. Rahul Yadav from ARK Technosolutions in association with IIT Madras.</p>
